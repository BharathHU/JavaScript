let num = 5;
console.log(num++);   // post-increment #5
console.log(num); //#6
console.log(++num);   // pre-increment #7

let a = 200;
console.log(a--);     // post-decrement //200
console.log(a);      // 199
console.log(--a);     // pre-decrement //#198

var number = 10;
number *= 2;
console.log(number);//#20

number /= 2;
console.log(number);// #10

number %= 2;
console.log(number);//#0

number += 2;
console.log(number); //#2

number -= 2;
console.log(number); // #0